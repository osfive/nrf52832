#include <sys/cdefs.h>
#include <sys/console.h>
#include <sys/systm.h>
#include <sys/malloc.h>
#include <sys/thread.h>

#include <machine/frame.h>

#include <arm/arm/nvic.h>
#include <arm/nordicsemi/nrf52832.h>

struct uarte_softc uarte_sc;

#define	UART_PIN_TX	7
#define	UART_PIN_RX	8
#define	UART_BAUDRATE	115200

void app_main(void);

static void
uart_putchar(int c, void *arg)
{
	struct uarte_softc *sc;
 
	sc = arg;
 
	if (c == '\n')
		uarte_putc(sc, '\r');

	uarte_putc(sc, c);
}

void
app_main(void)
{

	zero_bss();
	relocate_data();
	md_init();

	uarte_init(&uarte_sc, UART_BASE, UART_PIN_TX,
	    UART_PIN_RX, UART_BAUDRATE);
	console_register(uart_putchar, (void *)&uarte_sc);

	while (1)
		printf("Hello world!\n");
}
